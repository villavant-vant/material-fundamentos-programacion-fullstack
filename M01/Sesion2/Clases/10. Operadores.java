
public class Operadores {
    public static void main(String[] args) {

        // Sumatoria +
        int nro1 = 10; // Declara la variable, asignas el valor de 10
        int nro2 = 20;
        int resultado = nro1 + nro2;
        System.out.println("Suma: " + resultado);

        // Resta -
        nro1 = 30;
        nro2 = 15;
        resultado = nro1 - nro2;
        System.out.println("Resta: " + resultado);

        // Multiplicacion *
        nro1 = 5;
        nro2 = 8;
        resultado = nro1 * nro2;
        System.out.println("Mult: " + resultado);

        // Division entera /
        nro1 = 10;
        nro2 = 3;
        resultado = nro1 / nro2;
        System.out.println("Division Entera: " + resultado);

        // Division flotante /
        float numeroDecimal = 10.0f;
        nro2 = 3;
        float resultado2 = numeroDecimal / nro2;
        System.out.println("Division flotante: " + resultado2);


        // Modulo %
        nro1 = 11;
        nro2 = 2;
        resultado = nro1 % nro2;
        System.out.println("Modulo: " + resultado);




    }
}
